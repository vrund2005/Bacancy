// Write a program to check whether a number is:
// - Positive
// - Negative
// - Zero


let a = prompt("Enter a number = ")

if(a == 0){
    console.log("The number is ZERO")
}
else if(a < 0){
    console.log("The number is Negative")
}
else{
    console.log("The number is positive")
}